package server.client;

import java.io.IOException;
import java.io.OutputStream;
import java.net.Socket;

public class Client {
    public static void main(String[] args) throws IOException {
        Socket client = new Socket("192.168.1.117", 7766);
        byte[] bytes=new byte[]{
                0x76,0x76,0x76,0x76,     //头部
                0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x09, //设备id高8位
                0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x08, //设备id低8位
                0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x06, //回话id高8位
                0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x05, //回话id低8位
                0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x01, //序号id
                0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00, //保留字段
                0x00,0x00,0x00,0x05,                     //数据长度
                0x01,0x02,0x03,0x04,0x05,                //数据体
        };
        OutputStream out =client.getOutputStream();
        out.write(bytes);
        if(client != null){
            client.close();
        }
    }
}  